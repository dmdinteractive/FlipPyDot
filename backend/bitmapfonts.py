# RSS headlines and API text are littered with smart quotes and dashes that
# have no place in a 5x7 cell. Map them to the nearest ASCII rather than
# printing "?" in the middle of a headline.
_PUNCT = {
    "\u2014": "-", "\u2013": "-", "\u2212": "-", "\u2010": "-", "\u2011": "-",
    "\u2018": "'", "\u2019": "'", "\u201b": "'", "\u02bc": "'",
    "\u201c": '"', "\u201d": '"', "\u201e": '"', "\u00ab": '"', "\u00bb": '"',
    "\u2022": ".", "\u00b7": ".", "\u2027": ".",
    "\u00a0": " ", "\u2009": " ", "\u200a": " ", "\u202f": " ",
    "\u2044": "/", "\u00d7": "x",
}


def _compile(glyphs, w, h):
    """ASCII art -> {char: (h, w) uint8 array}."""
    out = {}
    for ch, rows in glyphs.items():
        a = np.zeros((h, w), dtype=np.uint8)
        for y, row in enumerate(rows):
            for x, c in enumerate(row):
                if c != " ":
                    a[y, x] = 1
        out[ch] = a
    return out


class BitmapFont:
    def __init__(self, key, name, glyphs, gw, gh, upper_only=False):
        self.key        = key
        self.name       = name
        self.gw         = gw          # native glyph width
        self.gh         = gh          # native glyph height
        self.upper_only = upper_only
        self._glyphs    = _compile(glyphs, gw, gh)
        self._fallback  = self._glyphs.get("?", np.zeros((gh, gw), dtype=np.uint8))

    def glyph(self, ch):
        if self.upper_only:
            ch = ch.upper()
        g = self._glyphs.get(ch)
        if g is not None:
            return g

        # Feeds are full of typographic punctuation that has no 5x7 glyph.
        sub = _PUNCT.get(ch)
        if sub is not None:
            return self._glyphs.get(sub, self._fallback)

        # Real data is full of accents — USGS reports quakes in "Guánica" and
        # "Bāmyān". A 5x7 cell has no room for diacritics, so fold to the base
        # letter: "GUANICA" is right, "GU?NICA" is just broken.
        folded = unicodedata.normalize("NFKD", ch)
        for c in folded:
            if unicodedata.combining(c):
                continue
            key = c.upper() if self.upper_only else c
            g = self._glyphs.get(key)
            if g is not None:
                return g
        return self._fallback

    def scale_for_height(self, px):
        """Largest integer scale whose rendered height fits within px."""
        return max(1, int(px) // self.gh)

    def height(self, scale=1):
        return self.gh * scale

    def measure(self, text, scale=1, tracking=1):
        """Width in pixels of `text`. `tracking` is the native-pixel gap."""
        lines = str(text).split("\n")
        if not lines:
            return 0
        widest = max(len(ln) for ln in lines)
        if widest == 0:
            return 0
        return (widest * self.gw + (widest - 1) * tracking) * scale

    def render(self, text, scale=1, tracking=1, leading=1):
        """Render (possibly multi-line) text to a tight numpy bitmap."""
        lines = str(text).split("\n")
        cell  = self.gw + tracking
        widest = max((len(ln) for ln in lines), default=0)
        if widest == 0:
            return np.zeros((self.gh * scale, 0), dtype=np.uint8)

        w = widest * cell - tracking
        line_h = self.gh + leading
        h = len(lines) * line_h - leading

        buf = np.zeros((h, w), dtype=np.uint8)
        for li, line in enumerate(lines):
            y0 = li * line_h
            for ci, ch in enumerate(line):
                x0 = ci * cell
                buf[y0:y0 + self.gh, x0:x0 + self.gw] = self.glyph(ch)

        if scale > 1:
            buf = np.repeat(np.repeat(buf, scale, axis=0), scale, axis=1)
        return buf


