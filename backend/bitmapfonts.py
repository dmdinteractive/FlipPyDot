"""
bitmapfonts.py — Hand-tuned pixel fonts for flipdot displays.

Flipdot panels have no antialiasing: a dot is either flipped or it isn't.
Scaling a vector font down to 7px and thresholding it produces mush, so the
glyphs here are drawn dot-by-dot at their native size and then scaled by
INTEGER factors only. A 5x7 face at scale 3 is a crisp 15x21 — every dot
still lands exactly on a dot.

Glyphs are authored as ASCII art ('#' = dot on) so they can be eyeballed and
corrected in the source. render_text() turns a string into a numpy bitmap.
"""

